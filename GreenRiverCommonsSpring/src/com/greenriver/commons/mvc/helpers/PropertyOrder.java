
package com.greenriver.commons.mvc.helpers;

/**
 * @author Miguel Angel
 */
public enum PropertyOrder {
    /**
     * Insert the property as the first
     */
    FIRST,
    /**
     * Insert the property as the last (add to the end)
     */
    LAST,
    /**
     * Insert the property before another one
     */
    BEFORE,
    /**
     * Insert the property after another one
     */
    AFTER
}
